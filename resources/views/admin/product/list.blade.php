    editForm.categorie.value = data.categorie.id;
    editForm.title.value = data.title;
    editForm.status.value = data.status;
    editForm.btnsub.value = "Edit Product";
    $("#add-new-data").addClass("show")
    $(".overlay-bg").addClass("show")

  }
